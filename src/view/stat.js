import SmartView from './smart.js';
import Chart from 'chart.js';
import ChartDataLabels from 'chartjs-plugin-datalabels';

const BAR_HEIGHT = 55;

const renderMoneyChart = (moneyCtx, points) => {

  const pointTypes = [...new Set(points.map((point) => point.eventType))];
  const costByType = pointTypes.map((type) => points.filter((point) => point.eventType === type).reduce((sum, point) => sum + point.basePrice, 0)).sort((a, b) => b - a);
  const randomColors = pointTypes.map(() => `#${Math.random().toString(16).substr(2,6)}`);
  console.log(pointTypes);
  console.log(costByType);
  console.log(randomColors);

  moneyCtx.height = BAR_HEIGHT * pointTypes.length;

  return new Chart(moneyCtx, {
    plugins: [ChartDataLabels],
    type: 'horizontalBar',
    data: {
      // labels: ['TAXI', 'BUS', 'TRAIN', 'SHIP', 'TRANSPORT', 'DRIVE'],
      labels: pointTypes.map((point) => point.toUpperCase()),
      datasets: [{
        data: costByType,
        // backgroundColor: '#ffffff',
        backgroundColor: randomColors,
        hoverBackgroundColor: '#ffffff',
        anchor: 'start',
      }],
    },
    options: {
      plugins: {
        datalabels: {
          font: {
            size: 13,
          },
          color: '#000000',
          anchor: 'end',
          align: 'start',
          formatter: (val) => `â‚¬ ${val}`,
        },
      },
      title: {
        display: true,
        text: 'MONEY',
        fontColor: '#000000',
        fontSize: 23,
        position: 'left',
      },
      scales: {
        yAxes: [{
          ticks: {
            fontColor: '#000000',
            padding: 5,
            fontSize: 13,
          },
          gridLines: {
            display: false,
            drawBorder: false,
          },
          barThickness: 44,
        }],
        xAxes: [{
          ticks: {
            display: false,
            beginAtZero: true,
          },
          gridLines: {
            display: false,
            drawBorder: false,
          },
          minBarLength: 50,
        }],
      },
      legend: {
        display: false,
      },
      tooltips: {
        enabled: false,
      },
    },
  });
};

